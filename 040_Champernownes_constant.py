import math

def ChampernownesConstant_FindTH(th):
    if th <= 9:
        return th
 
    minus = [1]
    result = ""

    # Build array that contains the range based on order of magnitude. E: if 123 is belowe the 2 value in the array but gretare then 1 value then 123 n_th is a number of 2 digits
    for i in range(1, len(str(th))):
        minus.append((9*math.pow(10,i-1) * (i)) + minus[i-1])


    # Find the order of magnitude of N_th and the th starting from 1*10^OOM
    for i in range(len(minus)-1,0,-1):
        if th-minus[i]>=0:
            th-= minus[i]
            index = i 
            break  

    index += 1       

    if index < 2:
        index = 2
         

    iChar = 0

    # The number is equal to 1*10^OOM + th/digit_lenght 
    if th%index != 0:
        num = math.pow(10,index-1) + math.floor(th/index)
    else:
        num = math.pow(10,index-1) + math.floor(th/index)

       
    iChar = th%index

    result = str(num)
    
    return result[int(iChar)]
        

